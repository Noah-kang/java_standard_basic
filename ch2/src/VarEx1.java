public class VarEx1 {
    // ch2-3 변수란? 변수의 선언과 저장
    public static void main(String[] args) {
        // 변수를 선언하고 사용함으로 간단하게 값을 유연하게 변경 가능합니다.
        int x = 4, y = 2;
        System.out.println(x+y); // 덧셈
        System.out.println(x-y); // 뺼셈
        System.out.println(x*y); // 곱셈
        System.out.println(x/y); // 나눗셈
    }
}
